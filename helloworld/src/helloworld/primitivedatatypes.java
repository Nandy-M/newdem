		short minshort=Short.MIN_VALUE;
		System.out.println("maxshort value is "+ maxshort);
		System.out.println("minshort value is "+ minshort);
		System.out.println();
		System.out.println("maxint value is "+Integer.MAX_VALUE);
		System.out.println("minint value is "+Integer.MIN_VALUE);
